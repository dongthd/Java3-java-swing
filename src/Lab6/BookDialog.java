    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Book b : list) {
            Object[] rows = new Object[]{b.getId(), b.getTitle(), b.getPrice()};
            model.addRow(rows);
        }
    }

