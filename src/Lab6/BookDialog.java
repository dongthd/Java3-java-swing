    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed

        BookDAO bdao = new BookDAO();
        int a = JOptionPane.showConfirmDialog(this, "Bạn muốn thoát?");
        if (a == 0) {
            bdao.disconnect();
            System.exit(0);
        }
    }//GEN-LAST:event_btnExitActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
      
        // Search
        list = db.getBookByTitle(txtKeyword.getText());
        if (list.size() == 0) {
            JOptionPane.showMessageDialog(this, "The book is notvaliable");
        } else {
            fillTable();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       
        int v = jTable1.getSelectedRow();
        int option = JOptionPane.showConfirmDialog(this, "Do you want to delete?");
        if (option == 0) {
            db.Delete(list.get(v).getId());
            list = db.getAllBook();
            fillTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

