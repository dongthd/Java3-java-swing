    private void cbbStandItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbStandItemStateChanged
        // sự kiện chọn cbb standardName -> fee tương ứng
        String standName = (String) cbbStand.getSelectedItem();
        double fee = standDAO.getFeeByName(dbConnect, standName);
        cbbFee.setSelectedItem(fee + "");
    }//GEN-LAST:event_cbbStandItemStateChanged

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Thêm mới
        Student sv = new Student();
        sv.setRegId(Integer.parseInt(txtRegID.getText()));
        sv.setName(txtName.getText());
        sv.setParentName(txtParent.getText());
        sv.setAddress(txtAddress.getText());
        Date date = Date.valueOf(txtDataReg.getText());
        sv.setRegDate(date);

        sv.setStandID(standDAO.getIDByName(dbConnect, (String) cbbStand.getSelectedItem()));
        sv.setPhone(txtPhone.getText());
        studentDAO.insert(dbConnect, sv);
        JOptionPane.showMessageDialog(this, "insert ok");
        listStudent = standDAO.getAllStudent(dbConnect);
        fillTable();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        DBConnect bdao = new DBConnect();
        int a = JOptionPane.showConfirmDialog(this, "Bạn muốn thoát?");
        if (a == 0) {
            bdao.disconnect();
            System.exit(0);
        }
    }//GEN-LAST:event_btnExitActionPerformed

