    void fillCBB() {
        for (Standard st : listStand) {
            cbbStand.addItem(st.getStandardName());
            cbbFee.addItem(st.getFee() + ""); // convert double -> String
        }
    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Student st : listStudent) {
            Object[] rows = new Object[]{st.getName(), st.getStandID()};
            model.addRow(rows);
        }
    }

